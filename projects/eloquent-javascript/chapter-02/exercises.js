function fizzBuzz() {
  // looping 15 times
  for (let i = 1; i <= 15; i++) {
    // checking to see if i is divisible by 5 and 3
    if (i % 5 === 0 && i % 3 === 0) {
      console.log('fizzbuzz');
      // checking to see if i is divisible by 5
    } else if (i % 5 === 0) {
      console.log('buzz')
      // checking to see if i is divisible by 3
    } else if (i % 3 === 0) {
      console.log('fizz')
    } else {
      console.log(i)
    }
  }
}

////////////////////////////////////////////////////////////////////////////////
// drawChessboard //////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function drawChessboard(input) {
  let chessBoard = "";

  // looping through the input twice
  for (let i = 0; i < input; i++) {
    for (let j = 0; j < input; j++) {
      if ((i + j) % 2 === 0) {
        // adding a black space for even iterations
        chessBoard += " ";
        // adding a hash simbol for odd iterations
      } else {
        chessBoard += "#";
      }
    }
    // returning a newline character
    chessBoard += "\n"
  }
  console.log(chessBoard)
}

